<div class="container-fluid project-description">
  <div class="row">
    <div class="col-12">
      <p>With the dramatic increase of IoT devices on home networks comes an even greater need to manage and protect them. The problem is two-fold. First, each device has unique settings and has the potential to introduce vulnerabilities that can affect not only the home network. Second, if devices are unsecured on home networks, they have the potential to interrupt network operations for ISPs to the point where entire portions of the Internet are down. As Minim looked to create a solution that would help protect consumers and ISPs, they turned to our team to help design the required interfaces.</p><p>As partners with the Minim team, we worked together to fully understand the challenges we had to solve in order to design the appropriate product experiences.</p><h3>ISPs have limited visibility into home networks.</h3><p> While their job is to provide a reliable service to customers at all times, there are many factors out of their control. Most ISPs can only tell if a the cable modem is on-line or off-line - limiting their ability to assist customers in troubleshooting problems or diagnosing broader network or device problems. They required a view that would aggregate information from different systems about the customer and their networked devices.</p><h3>Consumers are not technical experts.</h3><p>Sometimes smart homes aren’t really that smart. Although many consumers are quite adept at working with their personal devices, the sheer number and type of devices on home networks can be baffling. Each manufacturer has their own set-up and configuration instructions for their devices. Making matters more complex is that each device needs to be updated periodically to help keep it operational and secure. These steps are overwhelming for average home network users.</p><p>Our approach to designing an experience with Minim included multiple steps, including:</p>
