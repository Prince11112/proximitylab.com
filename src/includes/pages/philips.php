    <div class="col-lg-6">
      <p>Philips Lighting is the world’s leading provider of architectural lighting design and energy management systems. It’s technologies have given building owners the power and creativity to illuminate their buildings and landmarks with unlimited combinations of luminaires and color schemes. Philips has also created a software-based lighting & energy management system that allows building owners to realize significant energy savings by centrally monitoring & controlling their systems.</p>
        <p><strong>From proof-of-concept to production</strong> <br />Like many other enterprise applications, the software interface used to support these applications were more conceptual and weren’t designed to manage multiple buildings or to address individual controls with the required flexibility. Similarly, while the energy management system was incredibly powerful - giving building owners and operators the ability to modify the energy use of thousands of buildings simultaneously, the P.O.C. interface was un-intuitive and lacking. Our team was thrilled to be selected by Philips to help design a single experience that would support multiple user types for current and future requirements.
</p>
